package com.seocheon.fitian.model;

public class BoxModel {

	private String boxName;
	private String boxCode;
	private String boxContact;
	private String boxAddress;
	private String boxScript;
	
	public String getBoxName() {
		return boxName;
	}
	public void setBoxName(String boxName) {
		this.boxName = boxName;
	}
	public String getBoxCode() {
		return boxCode;
	}
	public void setBoxCode(String boxCode) {
		this.boxCode = boxCode;
	}
	public String getBoxContact() {
		return boxContact;
	}
	public void setBoxContact(String boxContact) {
		this.boxContact = boxContact;
	}
	public String getBoxAddress() {
		return boxAddress;
	}
	public void setBoxAddress(String boxAddress) {
		this.boxAddress = boxAddress;
	}
	public String getBoxScript() {
		return boxScript;
	}
	public void setBoxScript(String boxScript) {
		this.boxScript = boxScript;
	}
	
}
